const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const BartenderSchema = new Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

module.exports = Bartender = mongoose.model("Bartenders", BartenderSchema);
